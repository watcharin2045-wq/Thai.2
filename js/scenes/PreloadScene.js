// ========================================
// PreloadScene
// Thai Claw Game V4.0
// ========================================


export default class PreloadScene extends Phaser.Scene {


    constructor() {

        super("PreloadScene");

    }


    // ----------------------------------------
    // โหลด Assets
    // ----------------------------------------

    preload() {


        // ----------------------------------------
        // พื้นหลัง Loading
        // ----------------------------------------

        this.cameras.main.setBackgroundColor(
            "#9EDFFF"
        );


        // ----------------------------------------
        // ข้อความหัวข้อ
        // ----------------------------------------

        this.add.text(

            640,

            260,

            "🎮 ตู้คีบคำศัพท์",

            {

                fontSize: "48px",

                fontFamily: "Arial",

                fontStyle: "bold",

                color: "#0D47A1"

            }

        ).setOrigin(0.5);


        // ----------------------------------------
        // ข้อความ Loading
        // ----------------------------------------

        this.loadingText = this.add.text(

            640,

            350,

            "กำลังเตรียมเกม...",

            {

                fontSize: "28px",

                fontFamily: "Arial",

                color: "#000000"

            }

        ).setOrigin(0.5);


        // ----------------------------------------
        // กรอบ Loading
        // ----------------------------------------

        this.add.rectangle(

            640,

            430,

            500,

            30,

            0xFFFFFF

        );


        // ----------------------------------------
        // แถบ Loading
        // ----------------------------------------

        this.loadingBar = this.add.rectangle(

            390,

            430,

            0,

            24,

            0x42A5F5

        );


        // ----------------------------------------
        // Event Loading Progress
        // ----------------------------------------

        this.load.on(

            "progress",

            (value) => {


                this.loadingBar.width =

                    490 * value;


            }

        );


        // ----------------------------------------
        // เมื่อโหลดเสร็จ
        // ----------------------------------------

        this.load.on(

            "complete",

            () => {


                this.loadingText.setText(

                    "พร้อมแล้ว! กำลังเข้าสู่เกม..."

                );


            }

        );


    }


    // ----------------------------------------
    // สร้าง Scene
    // ----------------------------------------

    create() {


        // ----------------------------------------
        // รอเล็กน้อย
        // แล้วไป MenuScene
        // ----------------------------------------

        this.time.delayedCall(

            800,

            () => {


                this.scene.start(

                    "MenuScene"

                );


            }

        );


    }


}
